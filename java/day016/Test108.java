		// 함께 푼 내용 --------------------------
		for (i=1;i<a.length ;i++ )		// 웅~~~~ i → 뒤에서 비교 대상을 하나씩 줄여주는 역할
		{								//				1		2		3	4
			for(j=0; j<a.length-i; j++)	// 숑숑숑 j → 비교기준 인덱스
			{							//				0123	012		01	0
				//if(a[j] < a[j+1])		// 내림차순
				if(a[j] > a[j+1])		// 오름차순
				{
					//자리바꿈
					a[j]	= a[j] ^ a[j+1];
					a[j+1]	= a[j+1] ^ a[j];
					a[j]	= a[j] ^ a[j+1];
				}
			}
		}

		// 결과 출력
		System.out.printf("Sorted Data: ");
		for (int n: a )
			System.out.print(n + " ");
		System.out.println();
