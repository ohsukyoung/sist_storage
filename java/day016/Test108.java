			{
				if(a[j] > a[j+1])	// 오름차순 정렬
				{					
					// 자리 바꾸기
					a[j]	= a[j]^a[j+1];
					a[j+1]	= a[j+1]^a[j];
					a[j]	= a[j]^a[j+1];
				}
			}
			
